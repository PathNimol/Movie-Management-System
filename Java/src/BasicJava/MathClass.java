package BasicJava;

public class MathClass {
    public static void main (String[] args){
        float x = 10.3f;
        int y = 20;
        Math.max(x,y);// find the maximum values
        Math.min(x,y); // find the minimum values
        Math.random(); // random number
        Math.sqrt(x); // square
        Math.pow(x,y); //x exponent y
        Math.abs(x);
        Math.ceil(x); //round if <5 down , if > 5 up
        Math.floor(x);  //round only the number before .
        Math.round(x);//round if <5 down , if > 5 up
    }
}
